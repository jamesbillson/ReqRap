<h2>Rapid Web Requirements</h2>

<br />

<h3>Features</h3>
<p>

    
   
    
 <div class="row">   
<div class="span1">

 </div>
 <div class="span8">    
<div class="row">
    <h4>Use Case Based Analysis</h4> 
 </div>
<div class="row"><p class="well">
ReqRap provides a structured repository of Actors and Use Cases, with supporting Business Objects, 
Business Rules, User Interface definitions and User Input Forms. Use Cases are grouped into 
    Packages.  The inter-relation of the various elements supports traceability as well as automated 
    completeness checking.</p>
 </div>
<div class="row">
<h4>Collaboration Model</h4>
</div>
<div class="row"><p class="well">
ReqRap provides a collaborative space with messages and to-do lists.  
The various roles are modeled in the system to ensure that the various collaborators get
the information they need.  Invitation and management of project collaborators is managed
by the project owner.
    </p>
</div>
     
<div class="row">
<h4>Re-usable Library of Use Cases</h4>
 </div>
<div class="row"><p class="well">
ReqRap provides the ability to re-use common Use Cases between projects within a company. Use Cases can also be shared
publicly to allow their re-use by others.
    </p>
</div>

<div class="row">
<h4>Version Controlled</h4>
</div>
<div class="row"><p class="well">
All changes to the requirements model are version controlled.  The various objects in the model can be 
rolled back to previous states, and a change log is available for all objects.  A system of Releases
is used to control the publishing and circulation of the requirements model at project milestones.
    </p>
</div>
<div class="row">
<h4>Acceptance Walk-Through</h4>
</div>
<div class="row"><p class="well">
ReqRap provides a flow-by-flow presentation of the requirements to be used by business representatives 
in validating the model.  A feedback mechanism is incorporated to allow capture of results.
    </p>
</div>

<div class="row">
<h4>Test Case Generation and Management</h4>
</div>
<div class="row"><p class="well">
ReqRap automatically generates test cases from use cases including detailed verification steps of user inputs
and business rules.  The system iterates through the various alternate courses to provide complete
test coverage.  Test runs are entered directly to the system and feedback gathered at the time of
the test.
    </p>
</div>

</div>
</div>