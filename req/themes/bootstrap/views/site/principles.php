<h2>Rapid Web Requirements</h2>


<br />

<h3>Principles</h3>


    
 <div class="row">   
<div class="span1">

 </div>
 <div class="span8">    
<div class="row">
    <h4>Iteration</h4> 
 </div>
<div class="row"><p class="well">
ReqRap is designed to allow for iterative development of requirements.
Good requirements are developed by going over the same ground multiple times and
using realisations and decisions to feed back into your model. </p>
 </div>

<div class="row">
<h4>Right-sizing</h4>
 </div>
<div class="row"><p class="well">
The level of detail and the scope of the use case determine its size, 
and they are in a kind of equilibrium.  The correct level provides enough detail to 
capture all the business requirements, but without being over-prescriptive 
for the developers.  The process of iteration and ReqRap's flexibility allows the analyst to
split them if they are ‘too big’ or combine them if they are ‘too small’.</p>
</div>



<div class="row">
<h4>Completeness</h4>
</div>
<div class="row"><p class="well">
ReqRap encourages creation of ‘stub’ objects as the steps of a use case 
are created.  An analyst can keep their chain of thoughts going while making 
a note of interfaces, forms, rules and objects to be fleshed out later on.  
To keep track of these stub objects that need further definition the 
application is able provide intelligence around how complete the requirements model is.</p>
</div>
<div class="row">
<h4>Collaboration</h4>
</div>
<div class="row">
    <p class="well">
Reqrap provides a collaboration model with 5 roles: Owner, Contributor, Approver, Tester and Developer.
Owner: Owns the requirements project and has full control over it, all members of the company that owns a requirement project are considered an Owner.
Contributor:  Belongs to another company but has been invited to contribute to the project.  A contributor can create and change the requirements model, but cannot control the release cycle, or invite other collaborators.
Approver: An approver can view the requirements model in a release state, and can access the walk-throughs in order to provide review comments and to approve the use cases.  An approver can see the test run results.
Tester: A tester is able to view the requirements model in a release state and to access and run the test cases.
Developer: A developer is able to access released requirements, to create notes/queries and to maintain a to-do list.
</p>
</div>
     <div class="row">
<h4>Traceability</h4>
</div>
<div class="row"><p class="well">
ReqRap allows two way traceability. The analyst can see where objects 
have been ‘used’ and to see all the objects relate to a use case.  This 
prevents ‘orphaned’ objects, such as an interface that’s been defined but never used.</p>
</div>
</div>
